CREATE TABLE eod (
   symbol TEXT NOT NULL ON CONFLICT IGNORE,
   ts INTEGER NOT NULL ON CONFLICT IGNORE,
   open REAL NOT NULL ON CONFLICT IGNORE,
   close REAL NOT NULL ON CONFLICT IGNORE,
   high REAL NOT NULL ON CONFLICT IGNORE,
   low REAL NOT NULL  ON CONFLICT IGNORE,
   volume INTEGER NOT NULL ON CONFLICT IGNORE,
   UNIQUE (symbol, ts) ON CONFLICT REPLACE,
   CHECK (volume > 0) ON CONFLICT IGNORE,
);
